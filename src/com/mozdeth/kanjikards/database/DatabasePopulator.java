    static boolean m_update = false;
    
    //*********************************************************************************************
    
    public static void handleSingleKanji(SQLiteDatabase db, String kanji, String on, String kun, String english)
    {
        if (m_update)
        {
            // updating on/kun/english but not the Kanji/deck number
            // updating is sloooooow compared to inserting because it has to search.
            // Create a new map of values, where column names are the keys
            ContentValues values = new ContentValues();
            values.put(TableLabels.COLUMN_NAME_ON, on);
            values.put(TableLabels.COLUMN_NAME_KUN, kun);
            values.put(TableLabels.COLUMN_NAME_ENGLISH, english);
        
            // Which row to update, based on the Kanji
            String selection = TableLabels.COLUMN_NAME_KANJI + " = ?"; // the ? correlates to the arg in the next line
            String[] selectionArgs = { kanji };

            // Update the row where the Kanji matches
            db.update(
                TableLabels.TABLE_NAME,
                values,
                selection,
                selectionArgs);
        }
        else
        {
            // inserting kanji/on/kun/english/deckNumber
            // Create a new map of values, where column names are the keys
            ContentValues values = new ContentValues();
            values.put(TableLabels.COLUMN_NAME_KANJI, kanji);
            values.put(TableLabels.COLUMN_NAME_ON, on);
            values.put(TableLabels.COLUMN_NAME_KUN, kun);
            values.put(TableLabels.COLUMN_NAME_ENGLISH, english);
            values.put(TableLabels.COLUMN_DECK, DatabaseHelper.CARD_DECK_UNLEARNED);
            values.put(TableLabels.COLUMN_USER_MODIFIED, 0); // initially mark it as not modified
        
            // Insert the new row, returning the primary key value of the new row
            db.insert(TableLabels.TABLE_NAME, null, values);
        }
    }
    
    //*********************************************************************************************
    
    public static void updateDatabase(SQLiteDatabase db)
    {
        m_update = true;
        handleAllKanji(db);
    }
    
    //*********************************************************************************************
    
    public static void populateDatabase(SQLiteDatabase db)
    {
        m_update = false; // we are not updating, we are creating the database
        handleAllKanji(db);
    }
    
